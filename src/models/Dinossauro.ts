export default class Dinossauro{
    nome: string;
    altura: number;
    epoca: string;
    dieta: string;
    nomeCientifico: string;

    constructor (nome: string, altura: number, epoca: string, dieta:string, nomeCientifico:string){
      this.nome = nome;
      this.altura = altura;
      this.epoca =  epoca;
      this.dieta = dieta;
      this.nomeCientifico = nomeCientifico;
    }
}
    
